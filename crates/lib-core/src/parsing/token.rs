use std::ops::Range;

#[derive(Debug, Clone, Copy, PartialEq)]
pub enum TokenVariant {
    // simple tokens
    EndOfInput,
    LeftParen,
    RightParen,
    LeftSquare,
    RightSquare,
    DollarLeftBrace,
    DollarLeftParen,
    Dollar,
    At,
    Ampersand,
    AmperAmper,
    Bar,
    BarBar,
    Star,
    Dot,
    Comma,
    Plus,
    Minus,
    Caret,
    Colon,
    Semicolon,
    Tilde,
    Slash,
    Backslash,
    Equal,
    EqualEqual,
    Bang,
    BangEqual,
    Less,
    LessEqual,
    LessLess,
    Greater,
    GreaterEqual,
    GreaterGreater,
    SingleQuote,
    DoubleQuote,
    BackQuote,
    Newline,

    // variable lexeme
    Parameter,
    Name,
    Blob,
    Number,

    // reserved words
    Function,
    Namespace,
    Select,
    Case,
    Esac,
    Do,
    Until,
    While,
    If,
    Elif,
    Else,
    Fi,
    For,
    In,
    Then,
    Done,
    Time,
    LeftBrace,
    RightBrace,
    DoubleLeftSquare,
    DoubleRightSquare,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub variant: TokenVariant,
    pub span: Range<usize>,
    pub line: usize,
    pub column: usize,
}
